function LogoCard({ Logo }) {
  return (
    <div
      style={{
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        margin: "0 28px",
        padding: "16px 30px",
        border: "1px solid rgba(255,255,255,0.07)",
        borderRadius: "10px",
        background: "rgba(255,255,255,0.025)",
        minWidth: "110px",
        minHeight: "60px",
        transition: "all 0.32s cubic-bezier(0.16,1,0.3,1)",
        cursor: "default",
      }}
      onMouseEnter={(e) => {
        e.currentTarget.style.background = "rgba(255,255,255,0.065)";
        e.currentTarget.style.borderColor = "rgba(144,238,144,0.2)";
        e.currentTarget.style.boxShadow =
          "0 0 22px rgba(100,200,100,0.07), inset 0 1px 0 rgba(255,255,255,0.09)";
        e.currentTarget.style.transform = "translateY(-3px)";
        const svg = e.currentTarget.querySelector("svg");
        if (svg) {
          svg.style.opacity = "1";
          svg.style.filter = "drop-shadow(0 0 7px rgba(180,255,180,0.15))";
        }
      }}
      onMouseLeave={(e) => {
        e.currentTarget.style.background = "rgba(255,255,255,0.025)";
        e.currentTarget.style.borderColor = "rgba(255,255,255,0.07)";
        e.currentTarget.style.boxShadow = "none";
        e.currentTarget.style.transform = "translateY(0)";
        const svg = e.currentTarget.querySelector("svg");
        if (svg) {
          svg.style.opacity = "0.65";
          svg.style.filter = "none";
        }
      }}
    >
      <div
        style={{
          opacity: 0.65,
          transition: "opacity 0.3s ease, filter 0.3s ease",
        }}
      >
        {/* ✅ FIXED: Use React.createElement to ensure proper rendering */}
        {React.createElement(Logo)}
      </div>
    </div>
  );
}

